% hObject    handle to hitung (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% Ambil input
    Ip = str2double(get(handles.edit2, 'String'));
    Is = str2double(get(handles.edit3, 'String'));
    Np = str2double(get(handles.edit1, 'String'));
    Vp = str2double(get(handles.edit4, 'String'));

    % Perhitungan
    Ns = (Np * Ip) / Is;
    Vs = (Vp * Ns) / Np;

    % Output
    set(handles.output1, 'String', num2str(Ns));
    set(handles.output2, 'String', num2str(Vs));

